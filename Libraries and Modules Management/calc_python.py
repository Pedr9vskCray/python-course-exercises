"""
Módulo contendo 2 funções:

- sumnum(): retorna a soma dos 2 números recebidos
- subnum(): retorna a subtração dos 2 números recebidos
"""

def sumnum(num1, num2):
    """
    Retorna a soma de 2 números.
    """
    return num1 + num2

def subnum(num1, num2):
    """
    Retorna a subtração de 2 números.
    """
    return num1 - num2

